fav_languages = ["tamil", "german", "english", "japanese", "korea"]
print(f"My favourite languages are: {fav_languages}")

fav_languages.sort()
print(f"My fav languages in sorted: {fav_languages}")
fav_languages.sort(reverse=True)
print(f"My fav languages in reverse sorted: {fav_languages}")
print(f"My fav languages in sorted: {sorted(fav_languages)}")
fav_languages.reverse()
print(f"My fav languages in reverse order: {fav_languages}")
fav_languages.reverse()
print(f"My fav languages in reverse order: {fav_languages}")
print(f"The total no of my favourite languages are: {len(fav_languages)}")